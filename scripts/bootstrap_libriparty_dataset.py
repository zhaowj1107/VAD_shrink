#!/usr/bin/env python3
"""
Build a LibriParty-style folder layout under data/external/LibriParty/dataset using
SpeechBrain's create_custom_dataset recipe.

This path avoids Google Drive / gdown (often blocked) and avoids QUT background
downloads by setting backgrounds_root to null (Gaussian background, per recipe).

Prerequisites (OpenSLR, can be partial):
  data/external/LibriParty/downloads/LibriSpeech/{dev-clean,test-clean}/  (required)
  data/external/LibriParty/downloads/RIRS_NOISES/                       (required)

Optional: train-clean-100/ — only needed if you set n_sessions.train > 0.

Usage:
  PYTHONPATH=src python scripts/bootstrap_libriparty_dataset.py
  PYTHONPATH=src python scripts/bootstrap_libriparty_dataset.py --dev-sessions 50
"""

from __future__ import annotations

import argparse
import os
import runpy
import sys
from pathlib import Path


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[1]


def _patch_torchaudio() -> None:
    import torchaudio

    if not hasattr(torchaudio, "list_audio_backends"):
        torchaudio.list_audio_backends = lambda: []


def _write_yaml(
    path: Path,
    *,
    out_folder: Path,
    librispeech_root: Path,
    rirs_root: Path,
    dev_sessions: int,
    train_sessions: int,
    eval_sessions: int,
) -> None:
    # Train split uses dev-clean utterances when train-clean-100 is absent; keep
    # train_sessions=0 unless the user adds train-clean-100.
    text = f"""######################
# Generated by bootstrap_libriparty_dataset.py
######################

out_folder: {out_folder.as_posix()}
metadata_folder: {(out_folder / "metadata").as_posix()}
samplerate: 16000
save_wet_sources: false
save_dry_sources: false

librispeech_root: {librispeech_root.as_posix()}

librispeech_folders:
  train:
    - { (librispeech_root / "dev-clean").as_posix() }/
  dev:
    - { (librispeech_root / "dev-clean").as_posix() }/
  eval:
    - { (librispeech_root / "test-clean").as_posix() }/

rirs_noises_root: {rirs_root.as_posix()}
rirs_folders:
  - { (rirs_root / "simulated_rirs").as_posix() }/
  - { (rirs_root / "real_rirs_isotropic_noises").as_posix() }/
noises_folders:
  - { (rirs_root / "pointsource_noises").as_posix() }/

backgrounds_root: null

seed: 1234
split_factors:
  - 0.7
  - 0.2
  - 0.1

n_sessions:
  train: {train_sessions}
  dev: {dev_sessions}
  eval: {eval_sessions}
n_speakers: 2
interval_factor_speech: 20
interval_factor_noises: 10
max_length: 300
speech_lvl_min: -35
speech_lvl_max: -3
speech_lvl_mean: -15
speech_lvl_var: 3
imp_lvl_min: -40
imp_lvl_rel_max: 3
imp_lvl_mean: -33
imp_lvl_var: 2
background_lvl_min: -60
background_lvl_rel_max: -12
"""
    path.write_text(text, encoding="utf-8")


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Synthesize LibriParty from OpenSLR sources.")
    parser.add_argument(
        "--downloads-root",
        type=Path,
        default=_repo_root() / "data/external/LibriParty/downloads",
        help="Folder containing LibriSpeech/ and RIRS_NOISES/",
    )
    parser.add_argument(
        "--out-folder",
        type=Path,
        default=_repo_root() / "data/external/LibriParty/dataset",
        help="Output dataset root (train/dev/eval under here).",
    )
    parser.add_argument("--dev-sessions", type=int, default=5)
    parser.add_argument("--train-sessions", type=int, default=0)
    parser.add_argument("--eval-sessions", type=int, default=0)
    args = parser.parse_args(argv)

    repo = _repo_root()
    sb_gen = repo / "data/external/speechbrain/recipes/LibriParty/generate_dataset"
    if not sb_gen.is_dir():
        print(
            "Missing SpeechBrain recipe clone at data/external/speechbrain.\n"
            "Run:\n"
            "  git clone --depth 1 -b develop "
            "https://github.com/speechbrain/speechbrain.git data/external/speechbrain",
            file=sys.stderr,
        )
        return 1

    libri = args.downloads_root / "LibriSpeech"
    rirs = args.downloads_root / "RIRS_NOISES"
    for name, p in (
        ("LibriSpeech dev-clean", libri / "dev-clean"),
        ("LibriSpeech test-clean", libri / "test-clean"),
        ("RIRS_NOISES", rirs),
    ):
        if not p.is_dir():
            print(f"Missing {name}: {p}", file=sys.stderr)
            return 1

    if args.train_sessions > 0 and not (libri / "train-clean-100").is_dir():
        print(
            "train_sessions > 0 requires LibriSpeech train-clean-100 under downloads.",
            file=sys.stderr,
        )
        return 1

    args.out_folder.mkdir(parents=True, exist_ok=True)
    yaml_path = args.out_folder.parent / "bootstrap_libriparty_gaussian.yaml"
    _write_yaml(
        yaml_path,
        out_folder=args.out_folder.resolve(),
        librispeech_root=libri.resolve(),
        rirs_root=rirs.resolve(),
        dev_sessions=args.dev_sessions,
        train_sessions=args.train_sessions,
        eval_sessions=args.eval_sessions,
    )

    _patch_torchaudio()

    prev_argv = sys.argv[:]
    prev_cwd = os.getcwd()
    try:
        os.chdir(sb_gen)
        if str(sb_gen) not in sys.path:
            sys.path.insert(0, str(sb_gen))
        sys.argv = ["create_custom_dataset.py", str(yaml_path)]
        print("Running create_custom_dataset.py with", yaml_path)
        runpy.run_path(
            str(sb_gen / "create_custom_dataset.py"),
            run_name="__main__",
        )
    finally:
        os.chdir(prev_cwd)
        sys.argv = prev_argv

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
